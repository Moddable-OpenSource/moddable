				file.line("\t\t},");
				
				let char_name = { service_index:index, att_index:attributeIndex, encrypted, name:key };
				char_name.type = characteristic.type ? characteristic.type: "";
				char_names.push(char_name);

				++attributeIndex;
				
				// characteristic configuration descriptor
