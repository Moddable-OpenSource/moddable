				file.line("\t\t[", attributeIndex, "] = {");
				file.line(`\t\t\t${permissions},`);
				if (4 == uuid.length)
					file.line("\t\t\tQAPI_BLE_AET_CHARACTERISTIC_VALUE_16_E,");
				else
					file.line("\t\t\tQAPI_BLE_AET_CHARACTERISTIC_VALUE_128_E,");
