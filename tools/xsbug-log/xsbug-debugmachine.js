	onBroken(path, line, text) {
		// Auto-continue for non-interactive connections (e.g., mcsim)
		if (!this.interactive) {
			this.doGo();
			return;
		}

		this.broken = true;
		this.running = false;

		if (path) {
			this.currentPath = path;
			this.knownPaths.add(path);
		}
		if (line)
			this.currentLine = line;

		// Handle temporary breakpoint (until command)
		if (this.temporaryBreakpoint) {
			const temporaryBreakpoint = this.temporaryBreakpoint;
			this.doClearBreakpoint(temporaryBreakpoint.path, temporaryBreakpoint.line);
			// Remove from user breakpoints if it was added there
			const i = this.breakpoints.findIndex(bp => bp.path === temporaryBreakpoint.path && bp.line === temporaryBreakpoint.line);
			if (i >= 0)
				this.breakpoints.splice(i, 1);
			delete this.temporaryBreakpoint;
		}

		this.clearLine();

		const msg = text ? text.replace(/\n$/, '') : '';
		if (this.onBreakpointHit) this.onBreakpointHit();

		this.report('stopped', { 
			path: this.currentPath ?? '', 
			line: this.currentLine ?? 0, 
			reason: msg 
		}, undefined, (data) => {
			this.printLocation(data.reason);
			this.showPrompt();
		});
	}

	onLogged(path, line, data) {
		this.log += data;
		if (!this.logPath && path) {
			this.logPath = path;
			this.logLine = line;
			this.knownPaths.add(path);
		}
		if (this.log.endsWith("\n")) {
			if (this.log.startsWith("|:") && this.log.endsWith(":|\n")) {
				const payload = this.log.slice(2, this.log.length - 3);
				if (payload.startsWith("@filename ")) {
					const parts = payload.split(" ");
					if (parts[1])
						this.binaryName = parts[1];
				}
				else if (payload.startsWith("@close")) {
					if (this.binaryOutput) {
						fs.closeSync(this.binaryOutput);
						this.print(`#xsbug-log capture: ${this.binaryPath}`);
					}
					delete this.binaryOutput;
					delete this.binaryName;
					delete this.binaryPath;
				}
				else if (payload.startsWith("@")) {
					// ignore unknown directives
				}
				else if (payload) {
					if (!this.binaryOutput) {
						this.binaryPath = process.env.MODDABLE + "/build/tmp/" + (this.binaryName ? this.binaryName : "log.bin");
						this.binaryOutput = fs.openSync(this.binaryPath, "w+");
					}
					fs.writeSync(this.binaryOutput, Buffer.from(payload, "base64"), 0);
				}
			}
			else if (this.log.startsWith("# Break: ")) {
				// Suppress — onBroken will display this with full context
			}
			else if (this.log.startsWith("# Exception: ")) {
				if (this.exceptionsMode !== 'silent') {
					// Show exception with file/line like xsbug does
					const msg = this.log.slice(0, this.log.length - 1);
					this.print(msg, this.logPath, this.logLine, 'log');
				}
			}
			else {
				this.print(this.log.slice(0, this.log.length - 1), this.logPath, this.logLine, 'log');
			}
			this.log = "";
			this.logPath = null;
			this.logLine = null;
		}
	}

