	onViewChanged(name, items) {
		if (name === 'property') {
			if (items && items.length > 0) {
				const prop = items[0];
				if (prop && prop.value) {
					const hydrateNode = (nodes, visited = new Set()) => {
						if (!nodes) return;
						for (const node of nodes) {
							if (visited.has(node)) continue;
							visited.add(node);
							if (node.value === prop.value) {
								node.children = prop.children;
								node.flags = prop.flags;
							}
							if (node.children) {
								hydrateNode(node.children, visited);
							}
						}
					};
					hydrateNode(this.view.local);
					hydrateNode(this.view.global);
				}
			}
		}
		
		if (this.view[name] && name !== 'property') {
			const hydrateView = (oldItems, newItems, visited = new Set()) => {
				if (!oldItems || !newItems) return;
				for (const newItem of newItems) {
					if (visited.has(newItem.name)) continue;
					visited.add(newItem.name);
					const oldItem = oldItems.find(i => i.name === newItem.name);
					if (oldItem) {
						if (oldItem.children && (!newItem.children || newItem.children.length === 0)) {
							newItem.children = oldItem.children;
							// Also preserve the expanded flag if we hydrated children
							if (newItem.flags && newItem.flags.charAt(0) === '+') {
								newItem.flags = '-' + newItem.flags.slice(1);
							}
						} else if (oldItem.children && newItem.children) {
							hydrateView(oldItem.children, newItem.children, new Set());
						}
					}
				}
			};
			hydrateView(this.view[name], items);
		}
		if (name !== 'property') {
			this.view[name] = items;
		}
		if (name === 'frames') {
			for (const frame of items) {
				if (frame.path)
					this.knownPaths.add(frame.path);
			}
		}
		if (this.pendingViewCallback) {
			if (!this.pendingViewName || this.pendingViewName === name) {
				const pendingViewCallback = this.pendingViewCallback;
				this.pendingViewCallback = null;
				this.pendingViewName = null;
				pendingViewCallback(items, name);
			}
		}
	}

