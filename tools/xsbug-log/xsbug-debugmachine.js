	cmdRun() {
		if (this.connected) {
			this.promptShown = true;
			this.rl.question('The program being debugged has been started already.\nStart it from the beginning? (y or n) ', (answer) => {
				answer = answer.trim().toLowerCase();
				if (answer === 'y' || answer === 'yes') {
					console.log('Starting program...');
					this.doAbort();
				} else {
					console.log('Not confirmed.');
					this.promptShown = false;
					this.showPrompt();
				}
			});
		} else {
			console.log('No active program to restart.');
			this.showPrompt();
		}
	}

	cmdQuit() {
		if (this.connected)
			this.doAbort();
		process.exit(0);
	}
