void
des_keysched(const uint8_t *key, enum des_cipher_direction direction, des_subkey subkey)
{
	int i, j, l, m, n;
	uint8_t pc1m[56], pcr[56];
	uint32_t kn[32];

	for (i = 0; i < 56; i++) {
		l = c_read8(&pc1[i]);
		m = l & 07;
		pc1m[i] = (key[l >> 3] & c_read16(&bytebit[m])) ? 1 : 0;
	}
	for (i = 0; i < 16; i++) {
		m = (direction == des_cipher_decryption) ? (15 - i) : i;
		m <<= 1;
		n = m + 1;
		kn[m] = kn[n] = 0L;
		l = c_read8(&totrot[i]);
		for (j = 0; j < 28; j++, l++)
			pcr[j] = (l < 28) ? pc1m[l] : pc1m[l - 28];
		for (; j < 56; j++, l++)
			pcr[j] = (l < 56) ? pc1m[l] : pc1m[l - 28];
		for (j = 0; j < 24; j++) {
			if (pcr[c_read8(&pc2[j])]) kn[m] |= bigbyte[j];
			if (pcr[c_read8(&pc2[j+24])]) kn[n] |= bigbyte[j];
		}
	}

	for (i = 0; i < 32;) {
		uint32_t r0 = kn[i++];
		uint32_t r1 = kn[i++];
		uint32_t w;
		w = (r0 & 0x00fc0000) << 6;
		w |= (r0 & 0x00000fc0) << 10;
		w |= (r1 & 0x00fc0000) >> 10;
		w |= (r1 & 0x00000fc0) >> 6;
		*subkey++ = w;
		w = (r0 & 0x0003f000) << 12;
		w |= (r0 & 0x0000003f) << 16;
		w |= (r1 & 0x0003f000) >> 4;
		w |= (r1 & 0x0000003f);
		*subkey++ = w;
	}
}

void
des_process(const uint8_t *in, uint8_t *out, des_subkey subkey)
{
	uint32_t L, R, w, f;
	int i;

	L = *in++ << 24;
	L |= *in++ << 16;
	L |= *in++ << 8;
	L |= *in++;
	R = *in++ << 24;
	R |= *in++ << 16;
	R |= *in++ << 8;
	R |= *in;

	/* IP */
	w = ((L >> 4) ^ R) & 0x0f0f0f0f;
	R ^= w;
	L ^= w << 4;
	w = ((L >> 16) ^ R) & 0x0000ffff;
	R ^= w;
	L ^= w << 16;
	w = ((R >> 2) ^ L) & 0x33333333;
	L ^= w;
	R ^= w << 2;
	w = ((R >> 8) ^ L) & 0x00ff00ff;
	L ^= w;
	R ^= w << 8;
	R = (R << 1) | (R >> 31);
	w = (L ^ R) & 0xaaaaaaaa;
	L ^= w;
	R ^= w;
	L = (L << 1) | (L >> 31);

	for (i = 0; i < 8; i++) {
		w = (R << 28) | (R >> 4);
		w ^= *subkey++;
		f = SP7[w & 0x3f];
		f |= SP5[(w >> 8) & 0x3f];
		f |= SP3[(w >> 16) & 0x3f];
		f |= SP1[(w >> 24) & 0x3f];
		w = R ^ *subkey++;
		f |= SP8[w & 0x3f];
		f |= SP6[(w >> 8) & 0x3f];
		f |= SP4[(w >> 16) & 0x3f];
		f |= SP2[(w >> 24) & 0x3f];
		L ^= f;
		w = (L << 28) | (L >> 4);
		w ^= *subkey++;
		f = SP7[w & 0x3f];
		f |= SP5[(w >> 8) & 0x3f];
		f |= SP3[(w >> 16) & 0x3f];
		f |= SP1[(w >> 24) & 0x3f];
		w = L ^ *subkey++;
		f |= SP8[w & 0x3f];
		f |= SP6[(w >> 8) & 0x3f];
		f |= SP4[(w >> 16) & 0x3f];
		f |= SP2[(w >> 24) & 0x3f];
		R ^= f;
	}

	/* IP^{-1} */
	R = (R << 31) | (R >> 1);
	w = (L ^ R) & 0xaaaaaaaa;
	L ^= w;
	R ^= w;
	L = (L << 31) | (L >> 1);
	w = ((L >> 8) ^ R) & 0x00ff00ffL;
	R ^= w;
	L ^= (w << 8);
	w = ((L >> 2) ^ R) & 0x33333333;
	R ^= w;
	L ^= (w << 2);
	w = ((R >> 16) ^ L) & 0x0000ffff;
	L ^= w;
	R ^= (w << 16);
	w = ((R >> 4) ^ L) & 0x0f0f0f0f;
	L ^= w;
	R ^= w << 4;

	*out++ = R >> 24;
	*out++ = R >> 16;
	*out++ = R >> 8;
	*out++ = R;
	*out++ = L >> 24;
	*out++ = L >> 16;
	*out++ = L >> 8;
	*out = L;
}
