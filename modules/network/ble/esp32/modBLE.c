typedef struct {
	xsMachine	*the;
	xsSlot		obj;

	uint8_t *advertisingData;
	uint8_t *scanResponseData;
	uint8_t adv_config_done;
} modBLERecord, *modBLE;

void xs_ble_initialize(xsMachine *the)
{
	modBLE ble;
	ble = (modBLE)c_calloc(sizeof(modBLERecord), 1);
	if (!ble)
		xsUnknownError("no memory");
	xsmcSetHostData(xsThis, ble);
	ble->the = the;
	ble->obj = xsThis;
	
	// @@ check if we already do this
    ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES) {
        ESP_ERROR_CHECK(nvs_flash_erase());
        ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK( ret );

    ESP_ERROR_CHECK(esp_bt_controller_mem_release(ESP_BT_MODE_CLASSIC_BT));

    esp_bt_controller_config_t bt_cfg = BT_CONTROLLER_INIT_CONFIG_DEFAULT();
    ret = esp_bt_controller_init(&bt_cfg);
    if (ret) {
        ESP_LOGE(GATTS_TAG, "%s initialize controller failed\n", __func__);
        return;
    }

    ret = esp_bt_controller_enable(ESP_BT_MODE_BLE);
    if (ret) {
        ESP_LOGE(GATTS_TAG, "%s enable controller failed\n", __func__);
        return;
    }
    ret = esp_bluedroid_init();
    if (ret) {
        ESP_LOGE(GATTS_TAG, "%s init bluetooth failed\n", __func__);
        return;
    }
    ret = esp_bluedroid_enable();
    if (ret) {
        ESP_LOGE(GATTS_TAG, "%s enable bluetooth failed\n", __func__);
        return;
    }

    ret = esp_ble_gatts_register_callback(gatts_event_handler);
    if (ret){
        ESP_LOGE(GATTS_TAG, "gatts register error, error code = %x", ret);
        return;
    }
    ret = esp_ble_gap_register_callback(gap_event_handler);
    if (ret){
        ESP_LOGE(GATTS_TAG, "gap register error, error code = %x", ret);
        return;
    }
    ret = esp_ble_gatts_app_register(PROFILE_A_APP_ID);
    if (ret){
        ESP_LOGE(GATTS_TAG, "gatts app register error, error code = %x", ret);
        return;
    }
}

void xs_ble_close(xsMachine *the)
{
	modBLE ble = xsmcGetHostData(xsThis);
	xs_ble_destructor(ble);
	xsmcSetHostData(xsThis, NULL);
}

void xs_ble_destructor(void *data)
{
	modBLE ble = data;
	if (ble) {
		c_free(ble);
	}
}

